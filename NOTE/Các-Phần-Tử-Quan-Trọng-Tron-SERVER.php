 $_SERVER['PHP_SELF']

 Trả về tên file hoặc mã nguồn đang thực thi

 $_SERVER['GATEWAY_INTERFACE']

 Trả về phiên bản của CGI đang sử dụng

 $_SERVER['SERVER_ADDR']

 Trả về địa chỉ IP của host

 $_SERVER['SERVER_NAME']

 Trả về tên của server (ví dụ: www.w3schools.com)

 $_SERVER['SERVER_SOFTWARE']

 Trả về tên định danh (Như là Apache/2.2.24)

 $_SERVER['SERVER_PROTOCOL']

 Trả về tên và phiên bản của giao thức (Ví dụ: HTTP/1.1)

 $_SERVER['REQUEST_METHOD']

 Trả về phương thức sử dụng truy cập vào trang (Ví dụ: POST)

 $_SERVER['REQUEST_TIME']

 Trả về thời gian bắt đầu request (Ví dụ: 1377687496)
rồi
 $_SERVER['QUERY_STRING']

 Trả về chuỗi truy vấn

 $_SERVER['HTTP_ACCEPT']

 Trả về accept header từ yêu cầu truy cập hiện tại

 $_SERVER['HTTP_ACCEPT_CHARSET']

 Trả về một Accept_Charset header từ yêu cầu hiện tại truy cập (Ví dụ: utf-8,ISO-8859-1)

 $_SERVER['HTTP_HOST']

 Trả về host header từ một truy cập

 $_SERVER['HTTP_REFERER']

 Trả về URL đầy đủ của trang hiện tại

 $_SERVER['HTTPS']

 Trả về một tập lệnh truy vấn thông qua một giao thức an toàn

 $_SERVER['REMOTE_ADDR']

 Trả về địa chỉ IP nơi mà người dùng đang xem trang web

 $_SERVER['REMOTE_HOST']

 Trả về tên host nơi mà người dùng đang xem trang web

 $_SERVER['REMOTE_PORT']

 Trả về cổng

 $_SERVER['SCRIPT_FILENAME']

 Trả về đường dẫn tuyệt đối của mã nguồn đang thực thi

 $_SERVER['SERVER_ADMIN']

 Trả về giá trị cấu hình cho máy chủ web. Nếu mã nguồn của bạn chạy trên một máy ảo nó sẽ lấy giá trị định nghĩa cho cái máy ảo đó (such as someone@w3schools.com)

 $_SERVER['SERVER_PORT']

 Trả về cổng của máy chủ (ví dụ: 80)

 $_SERVER['SERVER_SIGNATURE']

 Trả về phiên bản của máy chủ

 $_SERVER['PATH_TRANSLATED']

 Trả về đường dẫn hệ thống tệp tin dựa vào mã nguồn hiện tại

 $_SERVER['SCRIPT_NAME']

 Trả về đường dẫn mã nguồn hiện tại

 $_SERVER['SCRIPT_URI']

 Trả về URI của trang hiện tại -->






